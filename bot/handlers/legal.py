"""TOS и политика обработки персональных данных (152-ФЗ)."""
from aiogram import Router
from aiogram.filters import Command
from aiogram.types import Message

router = Router()

TOS_TEXT = """📋 <b>Пользовательское соглашение GradeUp</b>

Используя бота @GradeUpInterviewBot, вы соглашаетесь с условиями ниже.

<b>1. Сервис</b>
GradeUp — инструмент для самостоятельной подготовки к IT-собеседованиям. Бот не гарантирует трудоустройство и не является кадровым агентством.

<b>2. Персональные данные (152-ФЗ)</b>
При использовании бота обрабатываются:
- Telegram user ID, имя пользователя
- Тексты ваших ответов на вопросы
- Загруженные резюме и описания вакансий (опционально)

Данные используются исключительно для работы сервиса. Не передаются третьим лицам. Хранятся на серверах в РФ/ЕС.

<b>3. Удаление данных</b>
Напишите /deletedata — все ваши данные будут удалены в течение 72 часов.

<b>4. Платная подписка</b>
Pro-подписка оплачивается через Telegram Stars. Возврат — согласно политике Telegram.

<b>5. Ограничение ответственности</b>
Сервис предоставляется "как есть". Разработчик не несёт ответственности за результаты собеседований.

Дата: май 2025 г."""

PRIVACY_TEXT = """🔒 <b>Политика обработки персональных данных</b>

В соответствии с Федеральным законом №152-ФЗ «О персональных данных».

<b>Оператор:</b> Индивидуальный предприниматель / физическое лицо, контакт: через @GradeUpInterviewBot

<b>Какие данные собираем:</b>
- Telegram ID и имя — для идентификации
- Тексты ответов — для AI-оценки и истории
- Резюме/вакансии — только если вы их отправили, только на время сессии

<b>Как используем:</b>
- Генерация персонализированных вопросов
- Оценка ответов через AI (OpenRouter/Anthropic)
- Статистика и геймификация внутри сервиса

<b>Кому передаём:</b>
- OpenRouter (anthropic) — тексты вопросов/ответов для AI-обработки
- Telegram — платёжные данные при покупке Stars
- Никому больше

<b>Срок хранения:</b> до удаления аккаунта по запросу пользователя

<b>Ваши права:</b>
- Запросить копию данных: /mydata
- Удалить все данные: /deletedata
- Отозвать согласие: /deletedata

Продолжая использовать бота, вы даёте согласие на обработку данных."""


@router.message(Command("tos"))
async def show_tos(message: Message) -> None:
    await message.answer(TOS_TEXT, parse_mode="HTML")


@router.message(Command("privacy"))
async def show_privacy(message: Message) -> None:
    await message.answer(PRIVACY_TEXT, parse_mode="HTML")


@router.message(Command("deletedata"))
async def delete_data(message: Message) -> None:
    await message.answer(
        "🗑 <b>Запрос на удаление данных принят.</b>\n\n"
        "Все ваши данные (профиль, история ответов, статистика) "
        "будут удалены в течение 72 часов.\n\n"
        "После удаления бот будет недоступен для вашего аккаунта.",
        parse_mode="HTML",
    )
    # TODO: реализовать реальное удаление через репозиторий


@router.message(Command("mydata"))
async def my_data(message: Message) -> None:
    await message.answer(
        f"📦 <b>Ваши данные в GradeUp:</b>\n\n"
        f"Telegram ID: <code>{message.from_user.id}</code>\n"
        f"Username: @{message.from_user.username or '—'}\n\n"
        f"История ответов и статистика хранятся в зашифрованной БД на сервере.\n"
        f"Для полной выгрузки или удаления: /deletedata",
        parse_mode="HTML",
    )
